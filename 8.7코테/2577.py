a = []
#숫자를 3개 입력받는다
for i in range(3):
    #리스트에 입력한 정수를 추가한다.
    a.append(int(input()))

# 곱셈을 한 뒤 count함수를 이용하기 위해 문자열로 바꾼다.
value = str(a[0] * a[1] * a[2])

# 0부터 9까지를 순회하며 몇번 사용되었는지 계산한다.
